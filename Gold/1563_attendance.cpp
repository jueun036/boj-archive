#include <stdio.h>

long long D[1001][2][3];

int main()
{
    int n, i, j;
    long long ans = 0;

    scanf("%d", &n);

    D[1][0][0] = 1;
    D[1][1][0] = 1;
    D[1][1][1] = 1;

    for (i = 2; i <= n; i++) {
        for (j = 0; j <= 2; j++) {
            D[i][0][0] += D[i-1][0][j];
            D[i][0][0] %= 1000000;
            D[i][0][0] += D[i-1][1][j];
            D[i][0][0] %= 1000000;
            D[i][1][0] += D[i-1][1][j];
            D[i][1][0] %= 1000000;
        }
        D[i][0][1] = D[i-1][0][0];
        D[i][0][2] = D[i-1][0][1];
        D[i][1][1] = D[i-1][1][0];
        D[i][1][2] = D[i-1][1][1];
    }

    for (i = 0; i <= 2; i++) {
        ans += D[n][0][i];
        ans %= 1000000;
        ans += D[n][1][i];
        ans %= 1000000;
    }

    printf("%lld", ans);

    return 0;
}
